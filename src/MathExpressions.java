
public class MathExpressions 
{
	public static void main (String[] args)
	{
		String flannel = "Too hot for flannels";
		System.out.println(flannel);
		String mask = " and mask";
		System.out.println(flannel + mask);
		//Concatenation = 2 strings added together
		
		//Variables
		
		int miles = 10; //positive or negative -> whole number
		int spiderman = 2;
		System.out.println (miles + "," + spiderman);
		System.out.println(miles + spiderman);
		System.out.println(miles - spiderman);
		System.out.println(miles * spiderman);
		System.out.println(miles / spiderman);
		System.out.println(miles % spiderman);
		
		double sydney = 9.2;
		double wife = 1.3;
		System.out.println(sydney * wife);
		System.out.println(sydney / wife);
		
		System.out.println(miles * (spiderman + miles));

		boolean ava = true;
		System.out.println(ava);
		
		char piper = '@';
		System.out.println(piper);

	}

}
